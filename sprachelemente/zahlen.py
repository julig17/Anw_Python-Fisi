#Darstellung der Dezimal 19 in Hexadezamial
#Zur Kennzeichnungvon hexadezimal-Darstellung verwendet python  #0x
print(hex(19))
#0x13
#Darstellung der Dezimal 19 in Oktal
#Zur Kennzeichnungvon Oktal-Darstellung verwendet python  #0o
print(oct(19))
#0o230
#Darstellung der Dezimal 19 in Binär
#Zur Kennzeichnungvon Binär-Darstellung verwendet python  #0b
print(bin(19))
#0b10011