'''Eingabe von einer Zahl , umwandeln in int 
und speichern in die Variable zahl1
'''
zahl1 = int(input("Zahl 1 eingeben:"))
zahl2 = int(input("Zahl 2 eingeben:"))
'''Zahlen addieren, mit drei multiplizieren 
und in Variable ergebnis speichern
'''
ergebnis = (zahl1 + zahl2) * 3
#ergebnis ausgeben
print ("(", zahl1, "+", zahl2, ")", "* 3 =", ergebnis)
